void ft_putchar(char c);
int main(int argc,char **argv)
{
	int i;
	int j;

	j = 1;
	i = 0;
	while (argc != 0)
	{
		i = 0;
		while (argv[j] [i] != '\0')
		{
			ft_putchar(argv[j] [i]);
			i++;
		}
		ft_putchar('\n');
		argc--;
		j++;
	}
}
